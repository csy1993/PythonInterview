# IOError：输入输出异常
# AttributeError：试图访问一个对象没有的属性
# ImportError：无法引入模块或包，基本是路径问题
# IndentationError：语法错误，代码没有正确的对齐
# IndexError：下标索引超出序列边界
# KeyError:试图访问你字典里不存在的键
# SyntaxError:Python代码逻辑语法出错，不能执行
# NameError:使用一个还未赋予对象的变量